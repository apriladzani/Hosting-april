public class Nested{
    public static void main(String[] args) {
        for (int lantai = 1; lantai <= 4; lantai++) {
            for (int kamar = 1; kamar <= 10; kamar++) {
                System.out.println("\nTawarkan dagangan di lantai " + lantai + " kamar " + kamar);
            }
        }
   }
}